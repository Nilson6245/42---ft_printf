#include"libft.h"

int	ft_lstsize(t_list *lst)
{
	int		a;
	t_list	*b;

	a = 1;
	if (lst == 0)
		return (0);
	b = lst;
	while (b->next)
	{
		b = b->next;
		a++;
	}
	return (a);
}
