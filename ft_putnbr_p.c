#include "ft_printf.h"

int	ft_putnbr_p(unsigned long n)
{
	int	i;

	i = 0;
	if (n == 0)
	{
		write(1, "0x0", 3);
		i += 3;
	}
	else
	{
		write(1, "0x", 2);
		i += 2;
		i += ft_putnbr_x(n);
	}
	return (i);
}
